void Bg::Update()
{
	m_isChipHit = false;

	IsWallHit = false;

	IsCeilingHit = false;

	Map();	

	if (m_player->IsClear)
	{
		m_isStage1 = false;

		m_isStage2 = true;
	}
}

void Bg::Draw()
{
	// マップチップを表示
	for (int y = 0; y < kChipNumY; y++)
	{
		for (int x = 0; x < kChipNumX; x++)
		{
			//データから配置するチップを決定する
			if (m_isStage1)
			{
				m_chipNo = kSetChipStage1[y][x];
			}
			if (m_isStage2)
			{
				m_chipNo = kSetChipStage2[y][x];
			}

			if (m_chipNo < 0)
			{
				//continueは繰り返し分(for,while)の中で使用する命令
				//continueが呼ばれたら以降の繰り返し処理は行わず、次のループに移行する
				continue;
			}

			// 0から始まる通し番号を
			// 上から何個目、左から何個目なのか、という情報に変換する必要がある
			// グラフィックに何個チップが含まれているか、という情報を使用して
			// 計算で求める
			int indexX = m_chipNo % m_graphChipNumX; // 左から何個目のチップか
			int indexY = m_chipNo / m_graphChipNumX; // 上から何個目のチップか

			// チップ番号から切り出し位置を計算する
			int cutX = indexX * kChipWidth;
			int cutY = indexY * kChipHeight;

			DrawRectGraph(x * kChipWidth, y * kChipHeight,
				cutX, cutY, kChipWidth, kChipHeight,
				m_handle, true);
		}
	}
	//画面全体を空色で埋め尽くす
	//DrawBox(0, 0, Game::kScreenWidth, Game::kScreenHeight, GetColor(160, 216, 239), true);
}

void Bg::Map()
{
	// マップチップを表示するテスト
	for (int y = 0; y < kChipNumY; y++)
	{
		for (int x = 0; x < kChipNumX; x++)
		{
			//データから配置するチップを決定する
			if (m_isStage1)
			{
				m_chipNo = kSetChipStage1[y][x];
			}
			if (m_isStage2)
			{
				m_chipNo = kSetChipStage2[y][x];
			}

			if (m_chipNo < 0)
			{
				//continueは繰り返し分(for,while)の中で使用する命令
				//continueが呼ばれたら以降の繰り返し処理は行わず、次のループに移行する
				continue;
			}

			m_chipLeft = x * kChipWidth;
			m_chipTop = y * kChipHeight;
			m_chipRight = x * kChipWidth + kChipWidth;
			m_chipBottom = y * kChipHeight + kChipHeight;

			//フラグ判定
			bool isPlayerLeft = (m_player->GetRight() <m_chipLeft);//Playerがチップの左側にいる
			bool isPlayerRight = (m_chipRight < m_player->GetLeft());//Playerがチップの右側にいる。
			bool isPlayerTop = (m_player->GetBottom() <m_chipTop);//Playerがチップの上にいる。
			bool isPlayerBottom = (m_chipBottom < m_player->GetTop());//Playerがチップの下にいる。		

			//地面に当たった時(7は地面)
			if (m_chipNo == 7)
			{
				if (!(isPlayerLeft || isPlayerRight || isPlayerTop || isPlayerBottom))
				{
					m_isChipHit = true;

					//当たった床の高さを記録
					m_chipTopGround = m_chipTop;

					/*
					Vec2 NextPos = m_player->GetPos();
					NextPos.y =m_ChiipTop + (m_player->GetBottom() - m_player->GetPos().y);
					m_player->SetPos(NextPos);
					*/
				}
				/*if (x * kChipWidth < m_player->GetLeft() && m_player->GetLeft() < x * kChipWidth + kChipWidth &&
					y * kChipHeight < m_player->GetTop() && m_player->GetTop() < y * kChipHeight + kChipHeight)
				{
					DrawString(10,160,"hit\n",0xffffff);
				}
				if (x * kChipWidth < m_player->GetRight() && m_player->GetRight() < x * kChipWidth + kChipWidth &&
					y * kChipHeight < m_player->GetTop() && m_player->GetTop() < y * kChipHeight + kChipHeight)
				{
					DrawString(10, 160, "hit\n", 0xffffff);
				}
				if (x * kChipWidth < m_player->GetLeft() && m_player->GetLeft() < x * kChipWidth + kChipWidth&&
					y * kChipHeight + kChipHeight < m_player->GetBottom() && m_player->GetBottom() < y * kChipHeight)
				{

				}*/
			}

			//天井に当たった時
			if (m_chipNo == 13)
			{
				//天井の判定
				if ((m_player->LinePos.y < m_chipBottom) && !(isPlayerLeft || isPlayerRight || isPlayerTop || isPlayerBottom))
				{
					//当たった天井の下側のy座標を記録
					m_chipBottomCeiling = m_chipBottom;

					IsCeilingHit = true;
				}
			}

			//壁に当たった時(29は壁)
			if (m_chipNo == 29)
			{
				//右に壁がある場合
				if ((m_player->LastPos.x <m_chipLeft) && !(isPlayerLeft || isPlayerRight || isPlayerTop || isPlayerBottom))
				{
					//当たった壁のX座標を記録
					m_chipLeftWall = m_chipLeft;

					IsWallHit = true;
					Vec2 NextPos = m_player->GetPos();
					NextPos.x =m_chipLeft - (m_player->GetRight() - m_player->GetPos().x) - 0.001f;
					m_player->SetPos(NextPos);
				}
				//左に壁がある場合
				if ((m_chipRight < m_player->LastPos.x) && !(isPlayerLeft || isPlayerRight || isPlayerTop || isPlayerBottom))
				{
					//当たった壁のX座標を記録
					m_chipRightWall = m_chipRight;

					IsWallHit = true;
					Vec2 NextPos = m_player->GetPos();
					NextPos.x =m_chipRight + (m_player->GetRight() - m_player->GetPos().x) + 0.001f;
					m_player->SetPos(NextPos);
				}				
			}			
		}
	}
}
